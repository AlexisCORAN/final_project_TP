    private double leerDouble(String texto) {
        
        if (texto == null || texto.trim().isEmpty()) {
            return 0.0;
        }
        
       
        try {
            return Double.parseDouble(texto.trim());
        } catch (NumberFormatException e) {
            // Si el usuario escribió letras ("abc"), devolvemos 0.0
            return 0.0;
        }
    }

